"""
Paper table — per-condition reportable numbers on the test split.

For every real condition (11) and one random control, emit:
    expected direction, tune-locked best coefficient,
    baseline logit, best logit, Δlogit,
    binary rate, Δrate (pp), cross-seed std (if multiseed present),
    Wilcoxon one-sided p_raw, Holm-adjusted p (family = 11 real conditions),
    secondary two-sided Wilcoxon p,
    degraded? flag.

Writes:
    paper/tables/table_1_results.csv
    paper/tables/table_1_results.tex
"""
import os
import sys

import numpy as np

_THIS = os.path.dirname(os.path.abspath(__file__))
sys.path.insert(0, _THIS)
from _paper_style import (
    RESULTS_DIR, PAPER_TBL_DIR, load_json, maybe_load_json,
    condition_order_for_paper, short_label, EXPECTED_DIRECTION,
    error_bar_for,
)


def fmt_p(p):
    if p is None:
        return "--"
    if p < 1e-15:
        return r"$< 10^{-15}$"
    if p < 1e-4:
        return f"${p:.1e}$".replace("e-0", r"\times 10^{-").replace("e-", r"\times 10^{-") + "}$"
    return f"{p:.4f}"


def fmt_p_csv(p):
    return "< 1e-15" if (p is not None and p < 1e-15) else (f"{p:.6g}" if p is not None else "")


def main():
    rates = load_json(f"{RESULTS_DIR}/sycophancy_rates_test.json")
    best  = load_json(f"{RESULTS_DIR}/best_coefs_tune.json")["best_coefs"]
    tests = load_json(f"{RESULTS_DIR}/statistical_tests_test.json")
    degraded = load_json(f"{RESULTS_DIR}/degradation_flags_test.json")
    wil = tests["primary_wilcoxon_best_vs_baseline"]

    baseline_rate = float(rates["assistant_axis"]["0.0"]["binary_rate"])
    baseline_logit = float(rates["assistant_axis"]["0.0"]["mean_syc_logit"])

    order = (["assistant_axis", "caa"]
             + [c for c in condition_order_for_paper()
                if c not in {"assistant_axis", "caa"}
                and not c.startswith("random_")]
             + ["random_0"])

    # CSV
    csv_rows = []
    csv_header = [
        "condition", "label", "expected_direction", "best_coef_tune",
        "baseline_logit", "best_logit_test", "delta_logit",
        "std_logit_multiseed", "n_seeds",
        "baseline_rate", "best_rate_test", "delta_rate_pp",
        "wilcoxon_alt", "wilcoxon_p_raw", "wilcoxon_p_adj",
        "wilcoxon_p_two_sided", "significant_after_mcc",
        "degraded_at_best",
    ]
    for cond in order:
        if cond not in rates:
            continue
        coef = float(best.get(cond, 0.0))
        k = f"{coef}"
        cell = rates[cond].get(k)
        if not cell:
            continue
        mean_l, std_l, _src = error_bar_for(cond, "test")
        if mean_l is None:
            mean_l = cell["mean_syc_logit"]
            std_l = None
        w = wil.get(cond, {})
        deg = bool(degraded.get(cond, {}).get(k, False))
        csv_rows.append([
            cond, short_label(cond), EXPECTED_DIRECTION.get(cond, "unsigned"),
            coef, baseline_logit, mean_l, mean_l - baseline_logit,
            std_l if std_l is not None else "",
            "multiseed" if std_l is not None else "",
            baseline_rate, cell["binary_rate"],
            (cell["binary_rate"] - baseline_rate) * 100,
            w.get("alternative", ""),
            fmt_p_csv(w.get("p_value_raw")),
            fmt_p_csv(w.get("p_value_adjusted")),
            fmt_p_csv(w.get("p_value_two_sided_raw")),
            bool(w.get("significant_after_mcc", False)),
            deg,
        ])

    import csv
    csv_path = os.path.join(PAPER_TBL_DIR, "table_1_results.csv")
    with open(csv_path, "w", newline="") as f:
        w = csv.writer(f)
        w.writerow(csv_header)
        for r in csv_rows:
            w.writerow(r)
    print(f"  saved {csv_path}")

    # LaTeX
    lines = []
    lines.append(r"% Auto-generated by paper/scripts/results_table.py")
    lines.append(r"\begin{table}[t]")
    lines.append(r"\centering")
    lines.append(r"\small")
    lines.append(r"\begin{tabular}{l l r r r r l r}")
    lines.append(r"\toprule")
    lines.append(r"Condition & Dir. & $c^*$ & $\Delta$logit & $\Delta$rate (pp) & $p_\text{Holm}$ & Alt. & deg. \\")
    lines.append(r"\midrule")
    for r in csv_rows:
        label = r[1]
        direction = r[2]
        coef = r[3]
        delta_logit = r[6]
        std_logit = r[7]
        delta_rate = r[11]
        p_adj = None
        try:
            p_adj = float(r[14]) if r[14] and r[14] != "< 1e-15" else 1e-300
        except Exception:
            p_adj = None
        alt = r[12]
        sig = bool(r[16])
        deg = bool(r[17])
        label_tex = label.replace("&", r"\&")
        std_str = ""
        if std_logit not in ("", None):
            try:
                std_str = rf" {{\scriptsize $\pm$ {float(std_logit):.3f}}}"
            except Exception:
                pass
        lines.append(
            f"{label_tex} & {direction} & {coef:+.0f} & "
            f"${delta_logit:+.3f}${std_str} & "
            f"${delta_rate:+.1f}$ & {fmt_p(p_adj)} "
            f"{'\\textsuperscript{*}' if sig else ''} & "
            f"{alt} & {'\\checkmark' if deg else ''} \\\\"
        )
    lines.append(r"\bottomrule")
    lines.append(r"\end{tabular}")
    lines.append(
        r"\caption{Per-condition sycophancy steering results on the held-out "
        r"test split. Best coefficient $c^*$ is locked from the tune split "
        r"using the direction-aware selector. $\Delta$logit / $\Delta$rate "
        r"are relative to the unsteered baseline ($\log p(\text{syc}) - "
        r"\log p(\text{hon})$ and binary sycophancy rate). $p_\text{Holm}$ is "
        r"the Holm-Bonferroni-adjusted one-sided paired Wilcoxon "
        r"signed-rank test in the direction indicated by ``Alt.''. Standard "
        r"deviations are across random seeds (multi-seed test split). "
        r"Degradation is flagged when the condition's binary rate is within "
        r"3\,pp of 50\% \emph{and} its logit is within 0.10 of the random "
        r"mean at the same coefficient.}"
    )
    lines.append(r"\label{tab:results}")
    lines.append(r"\end{table}")

    tex_path = os.path.join(PAPER_TBL_DIR, "table_1_results.tex")
    with open(tex_path, "w") as f:
        f.write("\n".join(lines) + "\n")
    print(f"  saved {tex_path}")


if __name__ == "__main__":
    main()
